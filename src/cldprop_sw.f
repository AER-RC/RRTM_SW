         IF (CLDFRAC(LAY) .GE. EPS) THEN
            ICLDATM = 1

C           Ice clouds and water clouds combined.
            IF (INFLAG .EQ. 0) THEN
               TAUCLDORIG_A = CLDDAT1(LAY)
               FFP = CLDDATMOM(NSTR,LAY)
               FFP1 = 1.0 - FFP
               FFPSSA = 1.0 - FFP * CLDDAT2(LAY)
               SSACLOUD_A = FFP1*CLDDAT2(LAY)/FFPSSA
